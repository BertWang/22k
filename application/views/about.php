    <blockquote>
    <p>「工作的愉悅，讓作品得以完美。」 -- 亞里斯多德</p>
    </blockquote>
    <div class="well">
<p>揭露22K，是一個專門揭露台灣不公平薪資的非謀利網站。起初，是由三個工程師在想著「如何改變社會」之餘所做出來的雛形網站（畢竟誰沒有領過22K呢？），隨著媒體曝光與雇主來信的抗議以後，報案量大幅增加，我們發現，它變成了一個長期的任務，一個有機會可以改善目前勞資不平等局面的重要任務。</p>

<p>我們收到了太多太多太多的感謝信，這些勞工向我們至上他們最高的敬意、為我們打氣加油、為我們吶喊，在這裡，我們謝謝所有的網友，我們將持續努力為大家服務，在下面的部份，我們也會提到我們未來的發展。</p>

<p>（恕我們不能曝光這些信件內容，因為我們擔心有心人士可以分析語調語氣等內容後，找到我們的支持者）</p>

<p>當然，本站的幾位工程師也是要顧著自己原來的工作、生活和家人，才能繼續有能力持續這個「全台灣勞工的希望」（一封打氣信的說法，當然我們可承擔不起）。也就這樣，我們工作速度一直跟不上大家爆料的速度，也不能在網站程式上更新太多。</p>

<p>基於越來越多勞工朋友將希望放置於我們身上，在不久的未來，22Kopendata將會轉型為「民間勞工局」的方式繼續運作，爆料的就不只是不平等薪資，還包含歧視、不平等對待、惡意雇主...等這些現象。我們將會一一透過合作的律師（歡迎律師與我們合作！）與我們的網站讓勞工知道我們永遠跟你們站在一起，讓雇主知道不合理的行為需要遭到批評與公開指責。</p>

<p>我們希望未來也能養起一個專職的網站客服員，以便更快速的處理大家的爆料與投訴。不過基於我們的想法跟本網站的宗旨，這個客服工程師的薪水最少也要30K，這不會是一個小數字。</p>

<p>但是上面說的這些技術更新、法律合作、專員，都會需要不少的專門人力與一些財力，目前在這方面我們沒有多餘的資源可以支持。</p>

